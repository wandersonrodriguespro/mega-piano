function randomNotification(items) {
  const index = Math.floor(Math.random() * items.length);
  return items[index];
}

function showNotification(container, side) {
  const notification = document.createElement("div");
  notification.classList.add(`notificacao-${side}`, "notificacao");

  notification.innerHTML = `
    <p>${randomNotification(arrayName)}</p>
    <p>${randomNotification(arrayValue)}</p>
  `;

  // Adiciona a notificação ao container
  container.innerHTML = "";
  container.appendChild(notification);

  // Ativa a transição
  setTimeout(() => {
    notification.style.opacity = 1;
    notification.style.transform = "translate(0)";
    notification.style.scale = 1;
  }, 0);

  // Oculta o popup após 3 segundos
  setTimeout(() => {
    notification.style.opacity = 0;
    notification.style.scale = 0;
    notification.style.transform = "translateY(100%)";
  }, 3000);
}

